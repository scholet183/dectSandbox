int __io_putchar(int ch) {
  char *linebreak = "\r\n";

  if (ch == '\n') {
    HAL_UART_Transmit(&huart2, (uint8_t *)linebreak, 2, HAL_MAX_DELAY);
  } else {
    HAL_UART_Transmit(&huart2, (uint8_t *)&ch, 1, HAL_MAX_DELAY);
  }

  return ch;
}

